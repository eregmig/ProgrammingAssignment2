## This function stores a matrix and its inverse matrix, and return a vector with 
## functions to set and get the matrix and set and get the inverse matrix.
makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setsolve <- function(solve) m <<- solve
  getsolve <- function() m
  list(set = set, get = get,
  setsolve = setsolve,
  getsolve = getsolve)
}

## This funtions returns the inverse matrix if already calculated and calculate it and return
## it in other case.
cacheSolve <- function(x, ...) {
  ## Return a matrix that is the inverse of 'x'
  m <- x$getsolve()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setsolve(m)
  m
}
